using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace FindDeadAnts
{
    public class FindDeadAnts
    {
        public static int DeadAntCount(string ants)
        {
            if (string.IsNullOrWhiteSpace(ants))
                return 0;

            return -1;
        }
    }
}
